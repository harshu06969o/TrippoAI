const tripModel = require("../models/trip.model")
const { generateTripItinerary, generateStaticPdf } = require("../services/ai.service")

/**
 * @name generateTripController
 * @description Generate AI trip, save to DB, and return data
 */
async function generateTripController(req, res) {
    try {
        const { destination, days, budget, travelers } = req.body

        // 1. Basic Validation
        if (!destination || !days || !budget) {
            return res.status(400).json({ message: "Destination, days, and budget are required" })
        }

        // 2. Call our powerful AI Service
        const aiItineraryData = await generateTripItinerary({ 
            destination, 
            days: parseInt(days), 
            budget, 
            travelers: parseInt(travelers) || 1 
        })

        // 3. Save to MongoDB
        const newTrip = await tripModel.create({
            user: req.user.id, // Assuming auth.middleware sets req.user
            destination,
            days,
            budget,
            travelers,
            estimatedTotalCost: aiItineraryData.estimatedTotalCost,
            hotelOptions: aiItineraryData.hotelOptions,
            itinerary: aiItineraryData.itinerary
        })

        // 4. Send response back to frontend
        res.status(201).json({
            message: "Trip generated successfully!",
            trip: newTrip
        })

    } catch (error) {
        console.error("Trip Generation Error:", error)
        res.status(500).json({ message: "Failed to generate trip", error: error.message })
    }
}

/**
 * @name getUserTripsController
 * @description Get all trips generated by the logged-in user (Optimized for Dashboard)
 */
async function getUserTripsController(req, res) {
    try {
        // Exclude heavy AI output fields for the list view
        const trips = await tripModel.find({ user: req.user.id })
            .sort({ createdAt: -1 })
            .select("-hotelOptions -itinerary"); 

        res.status(200).json({ trips })
    } catch (error) {
        res.status(500).json({ message: "Failed to fetch trips", error: error.message })
    }
}
/**
 * @name getTripByIdController
 * @description Get details of a specific trip
 */
async function getTripByIdController(req, res) {
    try {
        const tripId = req.params.id
        const trip = await tripModel.findOne({ _id: tripId, user: req.user.id })

        if (!trip) {
            return res.status(404).json({ message: "Trip not found" })
        }

        res.status(200).json({ trip })
    } catch (error) {
        res.status(500).json({ message: "Error fetching trip details", error: error.message })
    }
}

/**
 * @name downloadTripPdfController
 * @description Generate and send PDF for a specific trip
 */
async function downloadTripPdfController(req, res) {
    try {
        const tripId = req.params.id
        const trip = await tripModel.findOne({ _id: tripId, user: req.user.id })

        if (!trip) {
            return res.status(404).json({ message: "Trip not found" })
        }

        // Call static generator to design HTML and convert to PDF Buffer
        const pdfBuffer = await generateStaticPdf(trip)

        // Set headers so the browser knows it's downloading a PDF
        res.set({
            "Content-Type": "application/pdf",
            "Content-Disposition": `attachment; filename="${trip.destination}-Trip-Itinerary.pdf"`
        })

        res.send(pdfBuffer)

    } catch (error) {
        console.error("PDF Generation Error:", error)
        res.status(500).json({ message: "Failed to generate PDF", error: error.message })
    }
}

module.exports = {
    generateTripController,
    getUserTripsController,
    getTripByIdController,
    downloadTripPdfController
}